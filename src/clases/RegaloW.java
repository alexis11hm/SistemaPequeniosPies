    public void mostrarRegalo(DefaultTableModel modeloRegaloR,JTable tableRegaloR){
        
        try {
            tableRegaloR.setModel(modeloRegaloR);
            PreparedStatement ps = null;
            ResultSet rs = null;
            Conexion conn = new Conexion();
            java.sql.Connection con = conn.getConexion(Acceso.usuario,Acceso.contra);

            String sql = " select codbar_pro, cant_zapreg, fecha_zapreg, cve_con from zapatoregalado";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = (ResultSetMetaData) rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            
            modeloRegaloR.addColumn("Codigo de Barras");
            modeloRegaloR.addColumn("Cantidad");
            modeloRegaloR.addColumn("Fecha");
            modeloRegaloR.addColumn("Empleado");
            
            while (rs.next()) {
                Object[] filas = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modeloRegaloR.addRow(filas);
            }
            rs.close();
            ps.close();
            con.close();
        } catch (SQLException ex) {
            System.err.println(ex.toString());
        }
    }
    
    
